function createMap(template) {
    var m = new Map();
    // Fill in all keys
    template = typeof template === 'undefined' ? {} : template;
    var keys = Object.keys(template);
    for (var i = 0; i < keys.length; i++) {
        var key = keys[i];
        m[key] = template[key];
    }
    m.onCreate();
    return m;
}


